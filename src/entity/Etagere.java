package entity;

public class Etagere {
    private String boite;
    private String etagere;

    public Etagere(String boite, String etagere) {
        this.boite = boite;
        this.etagere = etagere;
    }

    public String getBoite() {
        return boite;
    }

    public String getEtagere() {
        return etagere;
    }
}
